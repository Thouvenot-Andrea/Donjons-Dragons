public class EquipementDefensif {
    String bouclier;
    String philtre;
    String nom;
    int level;

    public EquipementDefensif(String bouclier, String philtre, String nom, int level) {
        this.bouclier = bouclier;
        this.philtre = philtre;
        this.nom = nom;
        this.level = level;
    }

}